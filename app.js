document.getElementById('deedline-Button').addEventListener("click",function(event){
    event.preventDefault();
    alert("Board updated succesfully")

    const taskvalue=document.getElementById("task-value").innerText;
    let convertTaskValur=parseInt(taskvalue)

    const checkboxValue=document.getElementById('checkboxValue').innerText;
    let convertcheckboxValuemain=parseInt(checkboxValue)

    let convertcheckboxValue=convertcheckboxValuemain+1
    document.getElementById('checkboxValue').innerText=convertcheckboxValue;
    
    let historyadd=document.getElementById('historyadd')

    let convertTaskValue=convertTaskValur-1;
    document.getElementById("task-value").innerText=convertTaskValue;

   this.disabled=true;
   document.getElementById('deedline-Button').classList.add("universal")
   
 let date=new Date();
 let hours=date.getHours()
 let minit=date.getMinutes()
 let secound=date.getSeconds();

 let time=""+hours + ":  "+ minit + ":"+ secound +"" +"sec";

 let h3=document.createElement('h3')
 h3.innerText=`
    You hace completed the task file Mobile button issuse of ${time}
 
 `
historyadd.append(h3)

})

